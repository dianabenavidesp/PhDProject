classnoise<-function(positive, percent)
{
  for(rep in 1:30)
  {
      setwd(paste("C:/Users/dben652/workspace/AccGenSVM/data/caltech256/positives/",positive,"/10percent/data/data",rep,sep=""))
      fileName<-"sampletransfer251_145"
      data<-read.csv(paste(fileName,".csv.scale",sep=""), stringsAsFactors =FALSE, header=FALSE)
      matrix<-matrix(0,nrow(data), 1000)
      classes<-matrix(0,nrow(data), 1)
      
      positiveInstances<-NULL
      negativeInstances<-NULL
      numPositives<-1
      numNegatives<-1
      
      for(i in 1:nrow(data))
      {
        firstSplit<-strsplit(data[i,], split=":")
        
        secondSplit<-strsplit(firstSplit[[1]],split=" ")
        for(k in 2:length(secondSplit))
        {
          third<-as.numeric(unlist(secondSplit[[k]])[1])
          matrix[i,k-1]<-third
        }
        
        classes[i,1]<-as.numeric(unlist(secondSplit[[1]])[1])
        
        if(classes[i,1] == -1) { negativeInstances[numNegatives] = i; numNegatives <- numNegatives + 1} 
        if(classes[i,1] == 1) { positiveInstances[numPositives] = i; numPositives <- numPositives + 1}
      }
      data<-as.data.frame(matrix)
      
      samplePositives<-sample(positiveInstances,length(positiveInstances)*percent)
      sampleNegatives<-sample(negativeInstances,length(negativeInstances)*percent)
      
      for(i in 1:nrow(data))
      {
        if(i <= numPositives)
        {
          if(i %in% samplePositives)
          {
            classes[i,1] <- -1
          }
        }
        
        else
        {
          if(i %in% sampleNegatives)
          {
            classes[i,1] <- 1
          }
        }
      }
      
      data<-cbind(data, classes)
      data<-getfinaldataCaltech(data)
      data<-data[ order(-data[,1]), ]
      
      setwd(paste("C:/Users/dben652/workspace/AccGenSVM/data/caltech256/noise/class/",percent*100,"/data/data",rep,sep=""))
      write.table(data,paste(fileName, ".csv.scale", sep=""),row.names=FALSE, quote=FALSE, col.names=FALSE, sep=",")
      
      fixFilesNoiseClass(percent)
  }
}

fixFilesNoiseClass<-function(percent)
{
  setwd("C:/Users/dben652/workspace/AccGenSVM/data/caltech256/noise/class")
  i<-1
  for(k in 1:30)
  {
    filenames<-NULL
    filenames[i]<-paste(percent*100,"/data/data",k,"/sampletransfer251_145.csv.scale",sep="")
    i<-i+1

    for( f in filenames ){
      
      if(!is.na(f))
      {
        x <- readLines(f)
        y <- gsub( ":,", ":", x )
        yy <- gsub( ",", "\t", y )
        cat(yy, file=f, sep="\n")
      }
    }
  }
}

